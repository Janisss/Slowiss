<h1>Bedingungen</h1>
<p>Sprachkenntnisse - Um die Pflege- oder Betreuungstätigkeit erfolgreich wahrnehmen zu können, müssen Ihre Sprachkenntnisse mindestens dem elementaren Sprachniveau (A1) entsprechen. Je besser Sie die Fremdsprache des jeweiligen Landes beherrschen, desto höher sind Ihre Chancen auf einen gut bezahlten Job.</p>
<p>1. WELCHEM NIVEAU MÜSSEN MEINE FREMDSPRACHENKENNTNISSE ENTSPRECHEN?
Um die Pflege- oder Betreuungstätigkeit erfolgreich wahrnehmen zu können, müssen Ihre Sprachkenntnisse mindestens dem elementaren Sprachniveau (A1) entsprechen. Je besser Sie die Fremdsprache des jeweiligen Landes beherrschen, desto höher sind Ihre Chancen auf einen gut bezahlten Job.</p>

<p>2. MUSS ICH DIE VERMITTLUNGSGEBÜHREN BEZAHLEN?
Nein, bei uns fallen für die Betreuungskräfte weder die Anmelde- noch die Vermittlungsgebühren an. Unsere Dienstleistungen sind für die Bewerberinnen und Bewerber, gemäß dem § 25 Abs. 3 Gesetz Nr. 96/2013 GB, gültig seit 01.02.2013, kostenfrei. </p>

<p>3. WIRD DAS ZERTIFIKAT VOM PFLEGEKURS VERLANGT?
Ein akkreditiertes Zertifikat über einen Pflegekurs ist zwar ein großer Vorteil für Sie, jedoch keine Voraussetzung. Sollten Sie über kein Zertifikat verfügen, so ist ein Nachweis über zweijahrelange Arbeitserfahrung in der Betreuungsbranche und entsprechende Referenzen vorzulegen. </p>

<p>4. MUSS MAN ÜBER REFERENZEN VON DEN FAMILIEN ODER DEN GESUNDHEITSEINRICHTUNGEN VERFÜGEN?
Sofern Sie über ein gültiges Zertifikat über einen Pflegekurs verfügen, sind die Referenzen keine Voraussetzung mehr. Dennoch stellen diese offensichtlich einen großen Vorteil gegenüber andere Interessenten dar, und können den Prozess der Jobsuche wesentlich beschleunigen.</p>

<p>5. WIE HOCH IST DER MONATSLOHN?
Die Höhe des Lohnes ist von Ihren Sprachkenntnissen, Pflegeerfahrungen, sowie vom Gesundheitszustand des Betreuten, sowie vom gesamten Arbeitsaufwand abhängig. 
Der Monatslohn der Pflegerinnen und Pfleger in der Schweiz bewegt sich zwischen 2000,-CHF und 2100,-CHF Netto. Sollten zwei Personen betreut werden, beträgt der Monatslohn in der Regel ca. 2600,-CHF.</p>

<p>6. WIE UND WANN WIRD DER LOHN BEZAHLT?
Das Geld zahlt Ihnen die Familie des Betreuten am Ende jedes Monats bar aus.</p>

<p>7. GIBT ES ARBEITSANGEBOTE FÜR MÄNNER?		
Für die Herren gibt es in der Branche wesentlich weniger Arbeitsstellenangebote, weswegen die Bearbeitung einer solchen Bewerbung mehr Zeit in Anspruch nehmen kann. Dennoch gelten für die Bewerber die gleichen Voraussetzungen. </p>

